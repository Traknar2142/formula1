package ua.com.foxminded.task6.formula1;

public class IncorrectFileContentException extends Exception {

    public IncorrectFileContentException() {
        super();
    }

    public IncorrectFileContentException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public IncorrectFileContentException(String message, Throwable cause) {
        super(message, cause);
    }

    public IncorrectFileContentException(String message) {
        super(message);
    }

    public IncorrectFileContentException(Throwable cause) {
        super(cause);
    }
    

}
